num = int(input("Ingrese un numero (1 a 7): "))
 
if num == 1:
    print("Lunes")
elif num == 2:
    print("Martes") 
elif num == 3:
    print("Miercoles")
elif num == 4:
    print("Jueves")
elif num == 5:
    print("Viernes")
elif num == 6:
    print("Sabado")
elif num == 7:
    print("Domingo")
elif num == " ":
    print("No ingreso un numero")
else:
    print("Numero no valido")
